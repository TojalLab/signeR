    output$covariate_plot <- renderPlot({
      req(raw_user_data())
      feature_row <- input$user_data_rows_selected
      # print(feature_row)
      if (!is.null(feature_row)) {
        data <- raw_user_data()
        # print(head(data))
        col <- names(data[feature_row + 1])
        # print(col)
        labels <- data[[col]]
        # print(labels)
        if (class(labels) == "character") {
          # print("eh carac")
          sigs <- sigs_obj()
          if (is.null(sigs)) {
            return(NULL)
          }
          # print(head(sigs))
          if (!is.null(sigs)) {

            # if(!check_samples(data, sigs)){
            #   return(NULL)
            # }
            
            difexp_method <- diffexp_method()
            diffexp.quant <- diffexp_quant()
            diffexp.cutoff <- diffexp_cutoff()
            diffexp.padj <- diffexp_padj()

            # print(difexp_method)
            # print(diffexp.quant)
            # print(diffexp.cutoff)
            # print(diffexp.padj)

            DiffExp(
              sigs$SignExposures,
              labels = labels,
              quant = diffexp.quant, cutoff = diffexp.cutoff,
              p.adj = diffexp.padj
            )
          }
        }
      }
      return(NULL)
    })
