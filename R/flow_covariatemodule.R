covariate <- function(input,
                      output,
                      session,
                      signatures) {
  ns <- session$ns

  sigs_obj <- reactive({
    req(signatures())
  })

  check_samples <- function(df, sigs) {
    if(!validade_samples(df, sigs)){
      showModal(modalDialog(
        title = "Oh no!",
        paste0("Signatures samples and clinical data samples must be the same."),
        easyClose = TRUE,
        footer = NULL
      ))
      return(FALSE)
    }
    return(TRUE)
  }

  raw_user_data <- reactive({
    if (is.null(input$sample_data_file$datapath)) {
      return(NA)
    }
    result <- try(readr::read_tsv(input$sample_data_file$datapath))
    if (is.data.frame(result)) {
      if (!validate_clinical(result)) {
        showModal(modalDialog(
          title = "Oh no!",
          paste0("You must upload a valid clinical data file."),
          easyClose = TRUE,
          footer = NULL
        ))
        return(NA)
      } else {
        sigs <- sigs_obj()
        if(!is.null(sigs)){
          if(!check_samples(result, sigs)){
            return(NA)
          }
          return(result)
        } else {
          return(result)
        }
      }
    } else {
      return(NA)
    }
  })

  user_data <- reactive({
    # if (is.null(input$sample_data_file$datapath)) {
    #   return(NA)
    # }
    # result <- try(readr::read_tsv(input$sample_data_file$datapath))
    result <- raw_user_data()
    if (is.data.frame(result)) {
      ff <- rownames(t(result))[-1]
      df <- data.frame()

      for (f in ff) {
        fn <- f
        s <- result %>%
          select(1, f) %>%
          gather("key", "value", 2) %>%
          filter(!is.na(value)) %>%
          nrow()
        fq <- round(s / length(unique(result[[1]])) * 100, 3)
        s_na <- result %>%
          select(1, f) %>%
          gather("key", "value", 2) %>%
          filter(is.na(value)) %>%
          nrow()
        fq_na <- round(s_na / length(unique(result[[1]])) * 100, 3)
        class <- ifelse(
          class(result[[f]]) == "character", "categoric", "numeric"
        )
        data <- data.frame(
          "feature" = fn, "class" = class, "count" = paste0(s, " (", fq, "%)"),
          "missing" = paste0(s_na, " (", fq_na, "%)")
        )
        df <- rbind(df, data)
      }

      return(df)
    } else {
      return(NULL)
    }
  })

  feature_class <- reactive({
    req(raw_user_data())
    data <- raw_user_data()
    feature_row <- input$user_data_rows_selected

    if (!is.null(feature_row)) {
      col <- names(data[feature_row + 1])
      t <- data %>%
        select(col) %>%
        rownames_to_column() %>%
        select(-rowname) %>%
        with(class(get(col)))

      if (t == "character") {
        fqq <- data %>%
          select(col) %>%
          arrange(col) %>%
          with(unique(.))

        sss <- data %>%
          select(col) %>%
          group_by(fnn = get(col)) %>%
          filter(!is.na(fnn)) %>%
          summarise(n = n()) %>%
          mutate(freq = (n / sum(n)) * 100)

        df <- data.frame("groups" = NA, "n" = NA, "frequency" = NA)

        df %>%
          add_row(
            groups = sss$fnn,
            n = sss$n,
            frequency = paste0(round(sss$freq, 3), "%")
          ) %>%
          filter(!is.na(groups))
      } else if (t == "numeric") {
        df <- data %>%
          select(col) %>%
          summarise(
            min = min(get(col), na.rm = T),
            max = max(get(col), na.rm = T),
            mean = mean(get(col), na.rm = T),
            sd = sd(get(col), na.rm = T)
          )
      }
    } else {
      return(NULL)
    }
  })

  df_clinical <- reactive({
    # req(!is.na(user_data()), cancelOutput = T)
    user_data()
  })

